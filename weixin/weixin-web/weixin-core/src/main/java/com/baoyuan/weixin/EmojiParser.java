	/**
	 * Parse the text and change emoticons characters to chearsheet characters
	 * 
	 * @param text
	 *            Input text to be parsed
	 * @return Text parsed
	 */
	public static String emojiText(String text) {
		String returnTextString = text;
		// Pattern to match
		Pattern pattern = Pattern.compile("(\\[[a-z0-9]{5}\\])");
		Matcher matcher = pattern.matcher(text);
		while (matcher.find()) {
			String found = matcher.group();
			if (EmojiMap.get(found) == null)
				continue;
			returnTextString = returnTextString.replace(found,
					EmojiMap.get(found));
		}
		// Returning text
		return returnTextString;
	}

	/**
	 * Return the text with emoticons changed to android code
	 * 
	 * @param text
	 * @return
	 */
	public static String demojizedText(String text) {
		String returnTextString = text;
		// Pattern to match
		Pattern pattern = Pattern.compile("(\\:[^\\:]+\\:)");
		Matcher matcher = pattern.matcher(text);
		while (matcher.find()) {
			String found = matcher.group();
			if (IEmojiMap.get(found) == null)
				continue;
			returnTextString = returnTextString.replace(found,
					IEmojiMap.get(found));
		}
		// Returning text
		return returnTextString;
	}

